// box 그리기 함수, ch 문자로 (x1,y1) ~ (x2,y2) box를 그린다.
void draw_box1(int x1, int y1, int x2, int y2, char c)
{
	int x, y;

	for (x = x1; x <= x2; x += 1) {
		gotoxy(x, y1);
		printf("%c", c);
		gotoxy(x, y2);
		printf("%c", c);
	}
	for (y = y1; y <= y2; y++) {
		gotoxy(x1, y);
		printf("%c", c);
		gotoxy(x2, y);
		printf("%c", c);
	}
}
